// 1から100までの整数のうち、3の倍数と3がつく値を出力するプログラム
////////////////////////////////////////////////////////////////////////////////


// 3の倍数と3がつく値を保存する変数
$san = "";

// 1から100まで繰り返し
for($i = 1; $i <= 100; $i++) {
	// 3の倍数と3がつく値のみ保存
	if($i % 3 == 0 or strpos($i, '3') !== false) {
    	$san .= $i." ";
    }
}

// 3の倍数と3がつく値を出力
echo rtrim($san)

?>